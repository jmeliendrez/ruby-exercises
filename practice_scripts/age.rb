puts "How old are you?"

age = gets.chomp

ten = age.to_i + 10
twenty = age.to_i + 20
thirty = age.to_i + 30
forty = age.to_i + 40

puts "\nYou will be #{ten} in 10 years."
puts "\nYou will be #{twenty} in 20 years."
puts "\nYou will be #{thirty} in 30 years."
puts "\nYou will be #{forty} in 40 years."